import Head from "next/head";
import Image from "next/image";
import { Inter } from "next/font/google";
import styles from "@/styles/Home.module.css";
import { useState, useEffect, useRef } from "react";

// Components
import Header from "@/components/Header";
import Hero from "@/components/Hero";
import Link from "next/link";
import Featwork from "@/components/Featwork";
import About from "@/components/About";
import RollLoader from "@/components/RollLoader";
import Scene from "@/components/Burger/Scene";
import Scroller from "@/components/Scroller";
import BigText from "@/components/BigText";
import ExploreWork from "@/components/ExploreWork";

const inter = Inter({ subsets: ["latin"] });

export default function Home() {
  const scrollRef = useRef(null);

  const [statCont, setStatCont] = useState(false);
  const [abso, setAbso] = useState(false);
  const [heroScroll, setHeroScroll] = useState(false);
  const [bigScroll, setBigScroll] = useState(false);

  let firstY = 1.95;
  let secondY = 2.65;
  let thirdY = 4;

  function handleScroll(e) {
    let windLoc = window.scrollY / window.innerHeight;
    console.log(window.scrollY / window.innerHeight);
    if (windLoc >= firstY) {
      setStatCont(true);
      if (windLoc >= firstY && windLoc < secondY) {
        setBigScroll(true);
        setHeroScroll(true);
      } else if (windLoc >= secondY && windLoc < thirdY) {
        setAbso(true);
        setStatCont(false);
      } else {
        setStatCont(false);
        setAbso(false);
        setHeroScroll(false);
        setBigScroll(false);
      }
    } else if (windLoc >= 0.4 && windLoc < firstY) {
      setHeroScroll(true);
      setStatCont(false);
    } else {
      setStatCont(false);
      setAbso(false);
      setHeroScroll(false);
      setBigScroll(false);
    }
  }

  useEffect(() => {
    window.addEventListener("scroll", handleScroll);
  }, []);
  return (
    <>
      <Head>
        <title>GO Portfolio</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main ref={scrollRef} className={`${styles.main} ${inter.className}`}>
        <Hero heroScroll={heroScroll} bigScroll={bigScroll} />
        <BigText />
        <ExploreWork statCont={statCont} abso={abso} />
        <Featwork />
      </main>
    </>
  );
}
